// Language switching functionality
let currentLanguage = 'ja';

function initializeLanguage() {
    // Check browser language
    const browserLang = navigator.language.toLowerCase();
    const savedLang = localStorage.getItem('preferredLanguage');
    
    if (savedLang) {
        currentLanguage = savedLang;
    } else if (browserLang.startsWith('ja')) {
        currentLanguage = 'ja';
    } else {
        currentLanguage = 'en';
    }
    
    updatePageLanguage();
    updateLanguageToggle();
}

function switchLanguage() {
    currentLanguage = currentLanguage === 'ja' ? 'en' : 'ja';
    localStorage.setItem('preferredLanguage', currentLanguage);
    updatePageLanguage();
    updateLanguageToggle();
}

function updateLanguageToggle() {
    const toggleButton = document.querySelector('.language-toggle');
    if (toggleButton) {
        toggleButton.textContent = currentLanguage === 'ja' ? 'EN' : 'JP';
    }
}

function updatePageLanguage() {
    document.documentElement.lang = currentLanguage;
    
    // Update all translatable elements
    document.querySelectorAll('[data-translate]').forEach(element => {
        const keys = element.getAttribute('data-translate').split('.');
        let text = translations[currentLanguage];
        
        for (const key of keys) {
            text = text[key];
        }
        
        if (text) {
            element.textContent = text;
        }
    });
    
    // Update page title and meta tags
    if (translations[currentLanguage].meta) {
        document.title = translations[currentLanguage].meta.title || document.title;
        
        const metaDescription = document.querySelector('meta[name="description"]');
        if (metaDescription && translations[currentLanguage].meta.description) {
            metaDescription.content = translations[currentLanguage].meta.description;
        }
    }
}

// Initialize when DOM is ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initializeLanguage);
} else {
    initializeLanguage();
}